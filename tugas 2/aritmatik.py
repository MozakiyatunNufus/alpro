# buat 2 variabel
a = 5
b = 3

# penjumlahan(+)
hasil = a + b
print('Hasil penjumlahan : ',hasil)

# pengurangan(-)
hasil = a - b
print('Hasil pengurangan : ',hasil)

# perkalian(*)
hasil = a * b
print('Hasil perkalian :',hasil)

# pangkat(**)
hasil = a ** b
print('Hasil pengkat :',hasil)

# pembagian(/)
hasil = a / b
print('Hasil pembagian :',hasil)

# pembagian ke bawah(//)
hasil = a // b
print('Hasil pembagian kebawah :',hasil)

# sisa bagi(%)
hasil = a % b
print('Hasil sisa bagi :',hasil)

# precedence
hasil = 1 + 2 * 4 - 3
print('Hasil precedence :',hasil)

# precedence
hasil = (1 + 2) * 4 - 3
print('Hasil precedence :',hasil)
