print('OPERATOR AND')
print('Semua statement benar :',
       (5>3) and (7>5))
print('Salah satu statement salah :', 
       (5<3) and (7>5))
print('--------------')

print('OPERATOR OR')
print('Semua statement benar :',
        (5<3) or (7<5))
print('Salah satu statement salah :', 
        (5<3) or (7>5))
print('---------------')

print('OPERATOR NOT')
print('Semua statement benar :',not(5>3))
print('Salah satu statement salah :',not(5<3))
print('--------------')
